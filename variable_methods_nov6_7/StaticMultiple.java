package variable_methods_nov6_7;

public class StaticMultiple {

	public static void main(String[] args) {
		for(int num=1;num<=10;num++) {
			for(int mul=1;mul<=10;mul++) {
/* here multiply*/				System.out.println(num+ "*" + mul + "=" + num * mul);

			}
			System.out.println("------------");

		}

	}

}
